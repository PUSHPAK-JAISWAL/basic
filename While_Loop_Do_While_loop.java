public class While_Loop_Do_While_loop
{
    public static void main(String[]args)
    {
        int a=1;
        while( a<=5)
        {
            System.out.println("this is a while loop program");
            ++a;
        }
    

    
        int i=1;
        do
        {
            System.out.println("this is a do while loop");
            ++i;
        }
        while(i<7);
    }
}


