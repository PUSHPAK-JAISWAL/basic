public class Switch_case
{
    public static void main(String[]args)
    {
        int a= 400;
        switch(a)
        {
            case 100: System.out.println("100");
                        break;
            case 200:  System.out.println("200");
                        break;
            case 300:  System.out.println("300");
                        break;
            case 400:  System.out.println("400");
                        break;
        }
    }
}